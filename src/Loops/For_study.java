package Loops;

public class For_study {

	public static void main(String[] args) {
		//want to print no from 1 to 10
		for(int a=0;a<=10;a++)
		{
			System.out.println(a);
		}

		for(int a=2;a<=20;a=a+2)//want to print table of 2
		{
			System.out.println(a);
		}
		
		for(int a=10;a>=0;a--)//want to print 10 .....1
		{
			System.out.println(a);
		}
		for (int a=50;a>=5;a=a-5)//want to print 5 table in reverse order
		{
			System.out.println(a);
		}
		for (int a=1;a<=5;a++)//print velocity for 5 times
		{
			System.out.println("velocity");
		}
	}

}
